const month = 3
switch (month) {
  case 1:
    console.log("month is january");
    break;
  case 2:
    console.log("month is Feburary");
    break;
  case 3:
    console.log("month is March");   // month is March
    break;
  case 4:
    console.log("month is April");
    break;

  default:
    console.log("Default case match");
    break;
}